package day07;

public class RelationalOperators {

    public static void main(String[] args) {

        // > >= < <= == != the answer is always boolean true or false

        System.out.println(7>5);
        System.out.println(7 < -19); // less than
        System.out.println(5>=5); // more than or equal to
        System.out.println(7>=8); // greater or equal to
        System.out.println(4 <=5); //less than or equal to
        System.out.println(17>=8); // greater or equal


        System.out.println(5==5); // is used to check for equality and very different from single =
        System.out.println(6!=6); // used to check inequality, ! in coding means NOT


    }
}
