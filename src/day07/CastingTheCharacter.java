package day07;

public class CastingTheCharacter {

    public static void main(String[] args) {

        char garde = 'B' ;
        System.out.println( garde);

        int letterNumber = 'B' ; // the value of B in ASCI table is 66
        System.out.println(letterNumber);

        int letterNumber2 = 'b' ;
        System.out.println( letterNumber2);


        char myFirstChar = ( char ) 100 ;
        System.out.println(myFirstChar);

        int first = 'A' ;
        int second = 'H' ;
        int third = 'M' ;
        int fourth = 'E' ;
        int fifth = 'T' ;


        System.out.println( first);
        System.out.println( second);
        System.out.println( third);
        System.out.println( fourth);
        System.out.println( fifth);


    }
}
