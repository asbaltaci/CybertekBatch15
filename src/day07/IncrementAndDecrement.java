package day07;

public class IncrementAndDecrement {
    public static void main(String[] args) {


        // Increment decerement

        int offer = 2 ;
        offer +=1;
        ++ offer;
        System.out.println(offer);

        int apple = 10 ;
        apple +=1 ;
        ++ apple ;
        -- apple ;


        System.out.println(apple);





    }
}
