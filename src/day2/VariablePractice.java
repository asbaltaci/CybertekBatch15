package day2;

public class VariablePractice {
    public static void main(String[] args) {

        int mybirthyear = 1976;
        int areacode = 10102;
        int familycount = 5;
        int salary = 150000;

        System.out.println(" My birth year is "+mybirthyear);
        System.out.println(" My area code is " + areacode);
        System.out.println(" My family count is " +familycount);
        System.out.println(" My desired salary is "+ salary);
        System.out.println(" My birth year is " + mybirthyear + " and " + "\n" + " my family count is " + familycount);
    }
}
